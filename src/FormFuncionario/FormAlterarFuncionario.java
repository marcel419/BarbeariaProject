    private void jButtonBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBUSCARActionPerformed
        // TODO add your handling code here:

        try {
            Funcionario filtro = new Funcionario();

            FuncionarioBanco dados = new FuncionarioBanco();
            this.listaFuncionarios = dados.listar(filtro);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"CODIGO","MATRICULA", "NOME", "CPF",  "EMAIL","NASCIMENTO", "TELEFONE"});
            for (Funcionario funcionario : this.listaFuncionarios) {

                DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                Date date = (Date) funcionario.getdataNascimento();
                String s = df.format(date);

                modelo.addRow(new Object[]{funcionario.getID(), funcionario.getNome(),funcionario.getCpf(),funcionario.getEmail(), funcionario.getdataNascimento(), funcionario.getTelefone()});
            }
            jTableLISTAR.setModel(modelo);
            //JOptionPane.showMessageDialog(this, "Aluno cadastrado");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButtonBUSCARActionPerformed

    private void jButtonEXCLUIRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEXCLUIRActionPerformed
        // TODO add your handling code here:

        try {
            Funcionario f = new Funcionario();

            f.setID(Integer.parseInt(jTextFieldCODIGO.getText()));

            FuncionarioNegocio negocio = new FuncionarioNegocio();
            negocio.removerFuncionario(f);
            JOptionPane.showMessageDialog(this, "Funcionario excluido");

            TxtNome.setToolTipText("");
            jFormattedTextFieldDATA.setText("");
            jFormattedTextFieldCPF.setText("");
            TxtTelefone.setText("");
            TxtEmail.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButtonEXCLUIRActionPerformed

