    private void jButtonALTERARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonALTERARActionPerformed
        // TODO add your handling code here:

        try {
            Funcionario f = new Funcionario();

            String dataString = jFormattedTextFieldDATA.getText();
            DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date data = new java.sql.Date(fmt.parse(dataString).getTime());

            if (jTextFieldCODIGO.getText().matches("[0-9]+") == false) {
                JOptionPane.showMessageDialog(this, "Informe apenas n√∫meros para o codigo");
                return;
            }
            f.setNome(TxtNome.getText());
            f.setdataNascimento(data);
            f.setCpf(jFormattedTextFieldCPF.getText());
            f.setTelefone(TxtTelefone.getText());
            f.setEmail(TxtEmail.getText());

            FuncionarioNegocio negocio = new FuncionarioNegocio();
            negocio.atualizarFuncionario(f);
            JOptionPane.showMessageDialog(this, "Funcionario alterado");

            TxtNome.setToolTipText("");
            jFormattedTextFieldDATA.setText("");
            jFormattedTextFieldCPF.setText("");
            TxtTelefone.setText("");
            TxtEmail.setToolTipText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_jButtonALTERARActionPerformed

